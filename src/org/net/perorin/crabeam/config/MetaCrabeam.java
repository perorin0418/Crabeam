package org.net.perorin.crabeam.config;

public class MetaCrabeam {

	private int frame_height = 0;
	private int frame_width = 0;
	private int frame_x = 0;
	private int frame_y = 0;
	private int no_width = 0;
	private int ss_width = 0;

	public int getFrame_height() {
		return frame_height;
	}

	public int getFrame_width() {
		return frame_width;
	}

	public int getFrame_x() {
		return frame_x;
	}

	public int getFrame_y() {
		return frame_y;
	}

	public int getNo_width() {
		return no_width;
	}

	public int getSs_width() {
		return ss_width;
	}

	public void setFrame_height(int frame_height) {
		this.frame_height = frame_height;
	}

	public void setFrame_width(int frame_width) {
		this.frame_width = frame_width;
	}

	public void setFrame_x(int frame_x) {
		this.frame_x = frame_x;
	}

	public void setFrame_y(int frame_y) {
		this.frame_y = frame_y;
	}

	public void setNo_width(int no_width) {
		this.no_width = no_width;
	}

	public void setSs_width(int ss_width) {
		this.ss_width = ss_width;
	}

}
